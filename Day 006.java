public class day006 {

    public static void main(String[] args) {
        // float: angka pecahan, butuh huruf f/F di akhir
        float berat = 45.5f;

        // double: angka pecahan lebih presisi (default untuk pecahan)
        double panjang = 162.75;

        // menampilkan hasil
        System.out.println("Berat   : " + berat + " kg");
        System.out.println("Panjang : " + panjang + " cm");
    }
}
