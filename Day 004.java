public class day004 {
    public static void main(String[] args) {
        
        // \n = baris baru
        System.out.println("Halo\nIni biodata saya");

        // \t = tab (spasi lebar)
        System.out.println("Nama\t: Oliv");
        System.out.println("NIM\t: D0223018");

        // \" = menampilkan tanda kutip
        System.out.println("Oliv berkata: \"saya dari Universitas Sulawesi Barat\"");

        // \\ = menampilkan backslash
        System.out.println("Contoh path: C:\\Users\\Oliv\\Documents");
    }
}
