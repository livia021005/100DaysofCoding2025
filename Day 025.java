public class Day025 {
    public static void main(String[] args) {
        int angka = 20;

        System.out.println("Nilai awal: " + angka);
        angka++;
        System.out.println("Setelah increment (angka++): " + angka);
        angka--;
        System.out.println("Setelah decrement (angka--): " + angka);
    }
}

